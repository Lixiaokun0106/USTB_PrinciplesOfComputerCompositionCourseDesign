.MODEL SMALL
.STACK
.DATA
    ARRAY DB 56H,99,37H,78,0B0H
    COUNT EQU ($-ARRAY)/TYPE ARRAY
.CODE
.STARTUP
    MOV CX,COUNT
    DEC CX
OUTLP:
    MOV DX,CX
    MOV BX,OFFSET ARRAY
INLP:
    MOV AL,[BX]
    CMP AL,[BX+1]
    JNA NEXT
    XCHG AL,[BX+1]
    MOV [BX],AL
NEXT:
    INC BX
    DEC DX
    JNZ INLP
    LOOP OUTLP
.EXIT 0
END

